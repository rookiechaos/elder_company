CHINESE_RE = re.compile(r"[\u4e00-\u9fff\u3400-\u4dbf]")


def should_process(path: Path) -> bool:
    if path in SKIP_FILES:
        return False
    if any(part in SKIP_DIRS for part in path.parts):
        return False
    return path.suffix == ".py"


def strip_bilingual_docstring_lines(text: str) -> str:
    """Drop Chinese-only lines in module docstrings when English line exists above."""
    lines = text.splitlines(keepends=True)
    out: list[str] = []
    in_module_doc = False
    for i, line in enumerate(lines):
        stripped = line.strip()
        if i == 0 and stripped.startswith('"""'):
            in_module_doc = True
            out.append(line)
            if stripped.count('"""') >= 2 and stripped != '"""':
                in_module_doc = False
            continue
        if in_module_doc:
            if stripped.endswith('"""') and not stripped.startswith('"""'):
                in_module_doc = False
                out.append(line)
                continue
            if stripped.endswith('"""') and stripped == '"""':
                in_module_doc = False
                out.append(line)
                continue
            if CHINESE_RE.search(stripped) and out:
                prev = out[-1].strip()
                if prev and not CHINESE_RE.search(prev) and not prev.startswith("#"):
                    continue
        out.append(line)
    return "".join(out)


def translate_content(text: str) -> str:
    for old, new in MULTILINE:
        text = text.replace(old, new)
    for old, new in sorted(PHRASES, key=lambda x: len(x[0]), reverse=True):
        if old and old in text:
            text = text.replace(old, new)
    text = strip_bilingual_docstring_lines(text)
    return text


def process_file(path: Path) -> bool:
    original = path.read_text(encoding="utf-8")
    if not CHINESE_RE.search(original):
        return False
    updated = translate_content(original)
    if updated != original:
        path.write_text(updated, encoding="utf-8")
        return True
    return False


def main() -> int:
    targets = [
        BACKEND / "api",
        BACKEND / "middleware",
        BACKEND / "services",
        BACKEND / "dependencies",
        BACKEND / "config",
        BACKEND / "main.py",
        BACKEND / "exceptions.py",
        BACKEND / "utils",
        BACKEND / "models",
        BACKEND / "schemas",
    ]
    modified: list[str] = []
    for target in targets:
        if target.is_file():
            if should_process(target) and process_file(target):
                modified.append(str(target.relative_to(BACKEND)))
        elif target.is_dir():
            for path in sorted(target.rglob("*.py")):
                if should_process(path) and process_file(path):
                    modified.append(str(path.relative_to(BACKEND)))

    print("MODIFIED:")
    for m in modified:
        print(m)
    print(f"\nModified count: {len(modified)}")

    remaining = []
    for path in sorted(BACKEND.rglob("*.py")):
        if not should_process(path):
            continue
        if CHINESE_RE.search(path.read_text(encoding="utf-8")):
            remaining.append(str(path.relative_to(BACKEND)))
    print(f"\nRemaining with Chinese: {len(remaining)}")
    for r in remaining:
        print(r)
    return 0


